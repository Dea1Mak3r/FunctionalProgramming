class Solution(object):
    def jump(self, nums):
        """
        :type nums: List[int]
        :rtype: int
        """
        n = len(nums)
        
        # Если массив содержит только один элемент, прыжков не требуется
        if n == 1:
            return 0
        
        # Используем жадный алгоритм (greedy) с BFS-подобным подходом
        jumps = 0  # количество прыжков
        farthest = 0  # самая дальняя позиция, достижимая с текущим количеством прыжков
        current_end = 0  # конец текущего "уровня" прыжков
        
        # Проходим по массиву (не доходим до последнего элемента, т.к. он - цель)
        for i in range(n - 1):
            # Обновляем самую дальнюю достижимую позицию
            farthest = max(farthest, i + nums[i])
            
            # Когда достигаем конца текущего "уровня" прыжков
            if i == current_end:
                jumps += 1  # делаем ещё один прыжок
                current_end = farthest  # обновляем конец следующего уровня
                
                # Если уже можем достичь последнего элемента, можем остановиться
                if current_end >= n - 1:
                    break
        
        return jumps

